public class StatementSwitch {
public static void main( String[] args ){
    int grade = 1;
    switch(grade){
    case 1:
        System.out.println( "A" );
        break;
    case 2:
        System.out.println("B" );
        break;
    case 3:
        System.out.println("C" );
        break;
    default:
        System.out.println("D");
    }
  }    
}
